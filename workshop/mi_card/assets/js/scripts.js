$('#slide1').click(function(){
  if ($('.target1').css('display') == 'none' && $('.fa-angle-up').css('display') == 'none') {
    $('.target1').slideDown('slow');
    $('.hideicon1').show();
    $('.icon1').hide();
  }else{
    $('.target1').slideUp('slow');
    $('.hideicon1').hide();
    $('.icon1').show();
  }
});

